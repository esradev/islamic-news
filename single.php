                <p class="text-lg">In Islam, the concept of community, or <em>ummah</em>, extends far beyond geographical boundaries or ethnic affiliations. It represents a spiritual brotherhood and sisterhood that binds Muslims together through shared faith, values, and mutual responsibility. This divine bond, established by Allah and exemplified by Prophet Muhammad (peace be upon him), serves as the foundation for a just and compassionate society.</p>

                <h2 class="text-2xl font-bold text-islamic-green mt-8 mb-4">The Quranic Foundation of Community</h2>
                
                <p>The Quran emphasizes the importance of community in numerous verses, establishing it as a fundamental aspect of Islamic life. Allah says in the Quran:</p>
                
                <blockquote class="border-l-4 border-islamic-green pl-6 py-4 bg-islamic-cream rounded-r-lg my-6">
                    <p class="text-lg italic text-gray-800">"And hold firmly to the rope of Allah all together and do not become divided. And remember the favor of Allah upon you - when you were enemies and He brought your hearts together and you became, by His favor, brothers."</p>
                    <cite class="text-sm text-gray-600 mt-2 block">- Quran 3:103</cite>
                </blockquote>

                <p>This verse beautifully illustrates how Allah transforms individual hearts into a unified community, bound by love, understanding, and shared purpose. The "rope of Allah" represents the divine guidance that keeps the community anchored in righteousness and prevents it from drifting into discord and division.</p>

                <h2 class="text-2xl font-bold text-islamic-green mt-8 mb-4">The Prophet's Model of Community Building</h2>

                <p>Prophet Muhammad (peace be upon him) demonstrated the practical implementation of community building when he established the first Islamic community in Medina. His approach included several key principles:</p>

                <ul class="list-disc pl-6 space-y-2 my-6">
                    <li><strong>Inclusivity:</strong> The community welcomed people from diverse backgrounds, including former enemies</li>
                    <li><strong>Mutual Support:</strong> The Ansar (helpers) shared their wealth and homes with the Muhajirun (emigrants)</li>
                    <li><strong>Justice:</strong> Equal treatment regardless of social status or tribal affiliation</li>
                    <li><strong>Consultation:</strong> Important decisions were made through shura (consultation) with community members</li>
                </ul>

